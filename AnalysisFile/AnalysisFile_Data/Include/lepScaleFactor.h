float getElecTTHSFErr(float eta, float pt, int year, bool up) {
    float max_err = std::max(getElecTTHSFErrPt(pt, year), getElecTTHSFErrEta(eta, year));
    if (up)
    {
        // sf_up = sf*max_err 
        // --> (sf + err_up*sf) = sf*max_err --> err_up = max_err - 1
        return (max_err - 1);
    }
    else
    {
        // sf_up = sf/max_err 
        // --> (sf - err_down*sf) = sf/max_err --> err_down = 1 - 1/max_err
        return (1 - 1./max_err);
    }
}
